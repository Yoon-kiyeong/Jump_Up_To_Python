#문자열 슬라이싱이란?
#그렇다면 "Life is too short, You need Python"문자열에서 단순히 한 문자만을 뽑아내는 것이 아니라 'Life', 'You' 같은 단어를 뽑아낼 수는 없을까?
#다음과 같이 하면 된다

